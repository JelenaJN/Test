package com.bootcampexcercise.module2.activity;

public class PrintNumWithWhile {
    public static void main(String args[]) {

        int i = 0;
        while (i < 100) {
            System.out.println(i);
            i = i + 2;
        }


        int a = 1;
        while (a < 100) {
            System.out.println(a);
            a = a + 2;

        }
    }
}